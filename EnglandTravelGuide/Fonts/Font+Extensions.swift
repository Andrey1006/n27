import SwiftUI

extension Font {

    static func interBlack(size: CGFloat) -> Font {
        .custom("Inter24pt-Black", size: size)
    }

    static func interBold(size: CGFloat) -> Font {
        .custom("Inter24pt-Bold", size: size)
    }

    static func interRegular(size: CGFloat) -> Font {
        .custom("Inter24pt-Regular", size: size)
    }
}
